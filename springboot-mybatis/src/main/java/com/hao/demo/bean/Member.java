package com.hao.demo.bean;

/**
 * @author weihao
 *
 */
public class Member
{
	private int age;
	private int id;
	private Double money;
	private String name;

	/**
	 * @return the age
	 */
	public int getAge()
	{
		return age;
	}

	/**
	 * @return the id
	 */
	public int getId()
	{
		return id;
	}

	/**
	 * @return the money
	 */
	public Double getMoney()
	{
		return money;
	}

	/**
	 * @return the name
	 */
	public String getName()
	{
		return name;
	}

	/**
	 * @param age
	 *                the age to set
	 */
	public void setAge(int age)
	{
		this.age = age;
	}

	/**
	 * @param id
	 *                the id to set
	 */
	public void setId(int id)
	{
		this.id = id;
	}

	/**
	 * @param money
	 *                the money to set
	 */
	public void setMoney(Double money)
	{
		this.money = money;
	}

	/**
	 * @param name
	 *                the name to set
	 */
	public void setName(String name)
	{
		this.name = name;
	}

}
